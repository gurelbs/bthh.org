export const combine =  () => {
 let ok = 0, faild = 0, list = [];  
 const l = hostages.map(x=>{
  const item = all.find(y=> y.name.trim() == x.name.trim() );
  if (!!item){
   ok += 1;
   console.log(`englishName: ${item.englishName} found for ${x.name}`);
   const combine = { ...x, englishName: item.englishName };
   console.log('new item: ', combine);
   return combine;
  } else {
   list.push(x.name);
   faild += 1;
   return x;
  }
 });
 console.log({ ok, faild, list }); 
 writeFile('./file.ts', JSON.stringify(l));

};


